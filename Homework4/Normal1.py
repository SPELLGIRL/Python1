# Вариант без регулярных выражений:


def find_lowercase(string: str) -> list:
    """
    Находит символ в нижнем регистре вокруг символов в верхнем регистре, объединяет
    их в группу и возвращает список данных значений
    :param string: Строка, в которой производится выборка значений
    :return: Новый список с символами в нижнем регистре.
    """
    item = ''
    new_list = []
    for i in string:
        if i.islower():
            item += i
        elif item:
            new_list.append(item)
            item = ''
    else:
        if item and not string.islower():
            new_list.append(item)

    return new_list


